pub fn rust_common_pitfalls() {
    // for 循环中使用外部数组
    let mut v = vec![1, 2, 3];
    for i in 0..v.len() {
        v.push(i);
        println!("{:?}", v);
    }
    // 我们的目的是创建一个无限增长的数组，往里面插入0..(看不懂该表达式的同学请查阅流程控制)的数值序列。
    // 看起来上面代码可以完成，因为随着数组不停增长，v.len()也会不停变大，但是事实上真的如此吗？
    // 这是因为：在 for 循环中,v.len只会在循环伊始之时进行求值，之后就一直使用该值。
}
